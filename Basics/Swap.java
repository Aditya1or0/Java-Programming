package Basics;

public class Swap {
    public static void main(String[] args) {
        int a = 1; int b = 2; int temp;
        temp = a;
        a = b;
        b = temp;
        System.out.println("value of a is " + a);
        System.out.println("Value of b is " +b);
        
    }
    
}
